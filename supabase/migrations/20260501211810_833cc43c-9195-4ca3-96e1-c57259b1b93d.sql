-- =========================================================================
INSERT INTO storage.buckets (id, name, public)
VALUES ('signed-agreements', 'signed-agreements', false)
ON CONFLICT (id) DO NOTHING;

INSERT INTO storage.buckets (id, name, public)
VALUES ('intake-uploads', 'intake-uploads', false)
ON CONFLICT (id) DO NOTHING;

-- signed-agreements: server-only. No public policies — service role bypasses RLS.

-- intake-uploads: public can INSERT (browser file uploads from intake form);
-- SELECT requires signed URL minted server-side.
CREATE POLICY "Anyone can upload to intake-uploads"
ON storage.objects
FOR INSERT
TO anon, authenticated
WITH CHECK (bucket_id = 'intake-uploads');

-- No public SELECT on intake-uploads — files are read only via signed URLs
-- generated by server functions using the service role.